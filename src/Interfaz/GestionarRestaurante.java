    public boolean existeLista(DefaultListModel modelo, String dato) {
        boolean validar = false;
        for (int i = 0; i < modelo.getSize(); i++) {
            if (modelo.getElementAt(i).equals(dato)) {
                validar = true;
            }
        }
        return validar;
    }

    public void llenar(Restaurante administrador) throws Exception {
        SimpleDateFormat dt1 = new SimpleDateFormat("yyyy-MM-dd");
        for (int j = 0; j < administrador.getClientes().contar(); j++) {
            if (administrador.getClientes().valorIndice(j).getVerificacionReserva()) {
                Date fecha = administrador.getClientes().valorIndice(j).getReserva().getFechaReserva();
                array.add(administrador.getClientes().valorIndice(j).getNombre() + " para el día: " + dt1.format(fecha));
            }
        }
        modelo.removeAllElements();
        for (int i = 0; i < array.size(); i++) {
            if(!existeLista(modelo, array.get(i).toString())){
            modelo.addElement(array.get(i));
            }
        }
        jLabelNumPersonas.setText(String.valueOf(administrador.getAforoAct()-1));
        jTextFieldAforo.setText(String.valueOf(administrador.getAforoMax()));
        jTextArea1.setText(administrador.getPoliticas());
    }

