    private void jButtonGuardarYSalirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarYSalirActionPerformed
        // TODO add your handling code here:
        boolean validarAforo;
        validarAforo = isNumeric(jTextFieldAforo.getText());
        if (jTextFieldAforo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese un valor en el campo del aforo por favor", "Advertencia", 1);
        }
        if (!validarAforo) {
            JOptionPane.showMessageDialog(null, "Ingrese un número en el campo del aforo por favor", "Advertencia", 1);
        }
        if (jTextArea1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Recuerde ingresar las politicas internas del restaurante", "Advertencia", 1);
        }
        if (!jTextFieldAforo.getText().isEmpty() && validarAforo && !jTextArea1.getText().isEmpty()) {
            administrador.setAforoMax(Integer.parseInt(jTextFieldAforo.getText()));
            administrador.setPoliticas(jTextArea1.getText());
            System.out.println(administrador.getPoliticas());
            System.out.println(administrador.getAforoMax());
            InterfazAdmin i = new InterfazAdmin();
            i.crear(administrador);
            this.setVisible(false);
            i.setVisible(true);
        }
    }//GEN-LAST:event_jButtonGuardarYSalirActionPerformed

