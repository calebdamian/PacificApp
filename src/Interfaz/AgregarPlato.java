    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        // TODO add your handling code here:
        boolean valorPlato;
        boolean valorDesc;
        boolean referencia;
        referencia = isNumeric(jTextFieldNumRef.getText());
        valorPlato = isNumeric(jTextFieldValorPlato.getText());
        valorDesc = isNumeric(jTextFieldDescuento.getText());
        if (jTextFieldNombrePlato.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "El apartado de nombre del plato esta vacio", "Advertencia", 0);
        }
        if (jTextFieldDescripcionPlato.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "El apartado de la descripción del plato esta vacio", "Advertencia", 0);
        }
        if (jTextFieldValorPlato.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "El apartado del valor del plato esta vacio", "Advertencia", 0);
        }
        if (!valorPlato) {
            JOptionPane.showMessageDialog(null, "Ingrese un número en el campo del valor del plato", "Advertencia", 0);
        }
        if (!valorDesc) {
            JOptionPane.showMessageDialog(null, "Ingrese un número en el campo de valor del descuento", "Advertencia", 0);
        }
        if (!validarDescuento()) {
            JOptionPane.showMessageDialog(null, "El descuento no puede ser mayor o igual al valor del platillo", "Advertencia", 0);
        }

        if (!referencia) {
            JOptionPane.showMessageDialog(null, "Ingrese un número en el campo del valor de referencia del plato", "Advertencia", 0);
        }

        if (!validarPlato()) {
            JOptionPane.showMessageDialog(null, "El plato ya existe en el sistema", "Advertencia", 0);
        }
        if (!jTextFieldNombrePlato.getText().isEmpty() && !jTextFieldDescripcionPlato.getText().isEmpty() && !jTextFieldValorPlato.getText().isEmpty() && !jTextFieldDescuento.getText().isEmpty() && valorPlato && valorDesc && validarDescuento() && validarPlato() && referencia) {
            Plato p1 = new Plato(jTextFieldNombrePlato.getText(), jTextFieldDescripcionPlato.getText(), Double.parseDouble(jTextFieldValorPlato.getText()),Integer.parseInt(jTextFieldNumRef.getText()));
            if(jCheckBoxDescuento.isSelected()){
               p1.setDescuento(Double.parseDouble(jTextFieldDescuento.getText()));
               //p1.calcularValorConDescuento(Double.parseDouble(jTextFieldDescuento.getText()));
               
            }else{
               p1.setDescuento(0);
            }
            //p1.calcularValorConDescuento(Double.parseDouble(jTextFieldDescuento.getText()));
            AgregarPlato2 i = new AgregarPlato2();
            i.crear(administrador, p1);
            this.setVisible(false);
            i.setVisible(true);
        }
    }//GEN-LAST:event_jButtonGuardarActionPerformed

