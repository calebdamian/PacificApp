    private boolean validarDescuento() {
        boolean retornar = true;
        if (isNumeric(jTextFieldDescuento.getText())) {
            if (Double.parseDouble(jTextFieldDescuento.getText()) >= Double.parseDouble(jTextFieldValorPlato.getText())) {
                retornar = false;
                return retornar;
            } else {
                return retornar;
            }
        }
        retornar = false;
        return retornar;
    }

    private boolean validarPlato() {
        boolean retornar = true;

        if (administrador.getMenu().buscarPlato(Integer.parseInt(jTextFieldNumRef.getText())) != null) {
            retornar = false;
            return retornar;
        } else {
            return retornar;
        }

    }

    private static boolean isNumeric(String cadena) {
        try {
            Double.parseDouble(cadena);
            return true;
        } catch (NumberFormatException nfe) {
            return false;
        }
    }

