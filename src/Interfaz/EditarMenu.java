    private void jButtonEliminarPlatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarPlatoActionPerformed
        // TODO add your handling code here:
        String ref = JOptionPane.showInputDialog(null, "Ingrese el numero de referencia del plato a eliminar:");
        boolean refe = isNumeric(ref);
        try {
            if (refe) {
                if (administrador.getMenu().buscarPlato(Integer.parseInt(ref)) == null) {
                    JOptionPane.showMessageDialog(null, "El plato no existe en el sistema", "Error", 0);
                }
                administrador.getMenu().eliminarPlato(Integer.parseInt(ref));
                JOptionPane.showMessageDialog(null, "El plato con referencia " + ref + " ha sido eliminado", "Mensaje", 1);

            } else {
                JOptionPane.showMessageDialog(null, "Ingrese un número de referencia por favor", "Error", 0);
            }
        } catch (Exception e) {
            System.out.println("Cancelacion");
        } 


    }//GEN-LAST:event_jButtonEliminarPlatoActionPerformed

    private void jButtonModificarPlatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModificarPlatoActionPerformed
        // TODO add your handling code here:
        String ref = JOptionPane.showInputDialog(null, "Ingrese el numero de referencia del plato a modificar:");
        boolean refe = isNumeric(ref);
        try {
            if (refe) {
                if (administrador.getMenu().buscarPlato(Integer.parseInt(ref)) != null) {
                    System.out.println("Entro a crear el modificar plato");
                    ModificarPlato m1 = new ModificarPlato();
                    m1.crear(administrador, administrador.getMenu().buscarPlato(Integer.parseInt(ref)));
                    m1.llenar();
                    this.setVisible(false);
                    m1.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "El plato ingresado no existe en el sistema", "Advertencia", 0);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese un número de referencia por favor", "Error", 0);
            }
        } catch (Exception e) {
            System.out.println("Cancelacion");
        }
    }//GEN-LAST:event_jButtonModificarPlatoActionPerformed

