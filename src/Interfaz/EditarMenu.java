    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            // TODO add your handling code here:
            Cola cola = administrador.getMenu().getPlatos().niveles();
            for (int i = 0; i < administrador.getMenu().getPlatos().contar(administrador.getMenu().getPlatos().getRaiz()); i++) {
                NodoArbol dato = cola.desencolar();
                int referencia = dato.getDato().getReferencia();
                String titulo = dato.getDato().getNombre();
                JPanel panel = new JPanel();
                JLabel label = new JLabel("MENÚ:");
                label.setFont(new java.awt.Font("Ebrima", 1, 14));
                JTextArea texto = new JTextArea("Referencia: " + referencia + "\nNombre del platillo: \n" + titulo);
                texto.setFont(new java.awt.Font("Ebrima", 0, 14));
                texto.setEditable(false);
                panel.add(label);
                panel.add(texto);
                JOptionPane.showMessageDialog(null, panel);
            }
        } catch (Exception ex) {
            Logger.getLogger(EditarMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed
